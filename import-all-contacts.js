async function importAllContacts() {
  const API_URL = "https://relationship-intelligence.vercel.app/api/people";

  console.log(`\nStarting import of ${allContacts.length} additional contacts...\n`);

  let successCount = 0;
  let failCount = 0;

  for (let i = 0; i < allContacts.length; i++) {
    const contact = allContacts[i];
    try {
      const response = await fetch(API_URL, {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify(contact),
      });

      if (response.ok) {
        successCount++;
        console.log(`✓ [${i+1}/${allContacts.length}] Added: ${contact.name}`);
      } else {
        failCount++;
        const error = await response.text();
        console.log(`✗ [${i+1}/${allContacts.length}] Failed: ${contact.name}`);
      }
    } catch (error) {
      failCount++;
      console.log(`✗ [${i+1}/${allContacts.length}] Error: ${contact.name}`);
    }

    // Small delay to avoid overwhelming the server
    await new Promise(resolve => setTimeout(resolve, 300));
  }

  console.log(`\n========================================`);
  console.log(`✓ Import Complete!`);
  console.log(`  Successfully added: ${successCount}`);
  console.log(`  Failed: ${failCount}`);
  console.log(`  Total in database: ${15 + successCount} contacts`);
  console.log(`========================================`);
  console.log(`\nVisit: https://relationship-intelligence.vercel.app/people`);
}

